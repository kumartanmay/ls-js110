/*
given a sentence, find the word from the sentence and highlight it by putting it between **
and changing it to upper case. Return the modified sentence.

ds:
input: string
output: string

other vars
array holding the words of the original string
array holding the words of the modified string

algo:
====
1. split the sentence into words
2. find the matching words and convert it into uppercase and place it between **
3. join the resulting array into a string
*/

function search(word, sentence) {
    return sentence.split(" ")
                    .map(elem => {
                        if (elem.toLowerCase() === word) return `*${word.toUpperCase()}*`
                        else return elem;
                    })
                    .join(" ");
}

